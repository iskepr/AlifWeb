export function تحليل_الشفرة(الشفرة) {
    const الكلمات = [
        "ارجع",
        "توقف",
        "استمر",
        "صح",
        "خطا",
        "خطأ",
        "عدم",
        "استورد",
        "اطبع",
        "اشعار",
        "طول",
        "ادنى",
        "اقصى",
        "ادخل",
        "اضف",
        "امسح",
        "ادرج",
        "مفاتيح",
        "عام",
        "صحيح",
        "عشري",
        "مصفوفة",
        "لاجل",
        "لأجل",
        "بينما",
        "اذا",
        "إذا",
        "اواذا",
        "اوإذا",
        "والا",
        "وإلا",
        "احذف",
        "حاول",
        "خلل",
        "نهاية",
        "دالة",

        "جلب",
        "من",
        "صنف",
        "_تهيئة_",

        // المكتبات
        "الوقت",
        "الرياضيات",

        //  الواجهة
        "صفحة",
        "نص",
        "رابط",
        "عمودي",
        "رأسي",
        "توسيط",
        "بطاقة",
    ];

    const نمط_الرموز = [
        { النوع: "مسافة", regex: /^[ ]+/ },
        { النوع: "مسافة", regex: /^[\t]+/ },
        { النوع: "تعليق", regex: /^#.*\n?/ },
        { النوع: "سطر_جديد", regex: /^\n/ },
        { النوع: "فاصلة_م", regex: /^(;|؛)/ },
        { النوع: "اقواس", regex: /^(\(|\)|\[|\]|\{|\})/ },
        { النوع: "فاصلة", regex: /^(,|،)/ },
        { النوع: "نقطة", regex: /^\./ },
        { النوع: "نقطتان", regex: /^:/ },
        { النوع: "رقم", regex: /^\-?\d+(\.\d+)?/ },
        { النوع: "نص", regex: /^م?(['"])(.*?)\1/ },
        {
            النوع: "رمز_حسابي",
            regex: /^(\+=|-=|\*=|\\=|\^=|\\\*=|\\\\=|==|!=|<=|>=|<|>|\+|\-|\*|\\\\|\\\*|\^|\\\^|\\|و|او|أو|في|ليس في|ليس)/,
        },
        {
            النوع: "معرف",
            regex: /^[a-zA-Z\u0600-\u06FF_][a-zA-Z0-9\u0600-\u06FF_]*/,
        },
        { النوع: "رمز", regex: /^_/ },
        { النوع: "علامة_إسناد", regex: /^=/ },
    ];

    let الرموز = [];
    let السطر = 1;
    let بداية_السطر = true;

    while (الشفرة.length > 0) {
        let متطابق = false;

        // معالجة المسافات فقط في بداية السطر
        const نمط_المسافات = [نمط_الرموز[0], نمط_الرموز[1]];
        for (const نمط of نمط_المسافات) {
            const تطابق = الشفرة.match(نمط.regex);
            if (تطابق) {
                const المسافة = تطابق[0];
                if (
                    بداية_السطر &&
                    (نمط === نمط_الرموز[0] ? المسافة.length > 1 : true)
                ) {
                    الرموز.push({ النوع: "مسافة", القيمة: المسافة, السطر });
                }
                الشفرة = الشفرة.slice(المسافة.length);
            }
        }

        for (const النمط of نمط_الرموز) {
            if (النمط.النوع === "مسافة") continue;
            const تطابق = الشفرة.match(النمط.regex);
            if (تطابق) {
                const القيمة = تطابق[0];
                if (النمط.النوع === "تعليق") {
                    if (القيمة.includes("\n")) السطر++; // تجاهل التعليق
                    بداية_السطر = true;
                } else if (النمط.النوع === "سطر_جديد") {
                    الرموز.push({ النوع: "سطر_جديد", القيمة: "\n", السطر });
                    السطر++;
                    بداية_السطر = true;
                } else if (النمط.النوع === "فاصلة_م") {
                    الرموز.push({ النوع: "سطر_جديد", القيمة: "\n", السطر });
                    بداية_السطر = true;
                } else {
                    if (
                        الشفرة.startsWith("والا") ||
                        الشفرة.startsWith("وإلا")
                    ) {
                        الرموز.push({ النوع: "كلمة", القيمة: "والا", السطر });
                        الشفرة = الشفرة.slice(4);
                        متطابق = true;
                        continue;
                    } else if (
                        الشفرة.startsWith("اواذا") ||
                        الشفرة.startsWith("أواذا")
                    ) {
                        الرموز.push({ النوع: "كلمة", القيمة: "اواذا", السطر });
                        الشفرة = الشفرة.slice(5);
                        متطابق = true;
                        continue;
                    } else if (الشفرة.startsWith("واجهة")) {
                        الرموز.push({ النوع: "معرف", القيمة: "واجهة", السطر });
                        الشفرة = الشفرة.slice(5);
                        متطابق = true;
                        continue;
                    }
                    // كلمات محجوزة vs معرف عادي
                    if (النمط.النوع === "معرف" && الكلمات.includes(القيمة)) {
                        الرموز.push({ النوع: "كلمة", القيمة, السطر });
                    } else {
                        الرموز.push({ النوع: النمط.النوع, القيمة, السطر });
                    }
                    بداية_السطر = false;
                }

                الشفرة = الشفرة.slice(القيمة.length);
                متطابق = true;
                break;
            }
        }

        // تخطي المسافات الغير مشاخدمة
        if (/^\s+$/.test(الشفرة)) {
            الشفرة = الشفرة.trimStart();
            continue;
        }

        if (!متطابق) {
            const error = new Error(
                `المحلل: " ${الشفرة.slice(0, 10)} " غير معروف في السطر ${السطر}`
            );
            throw error.message;
        }
    }

    return الرموز;
}
