export function تحليل_الشفرة(الشفرة) {
    const الكلمات = [
        "ارجع",
        "توقف",
        "استمر",
        "صح",
        "خطا",
        "خطأ",
        "عدم",
        "استورد",
        "اطبع",
        "اشعار",
        "طول",
        "ادنى",
        "اقصى",
        "ادخل",
        "اضف",
        "امسح",
        "ادرج",
        "مفاتيح",
        "عام",
        "صحيح",
        "عشري",
        "مصفوفة",
        "لاجل",
        "لأجل",
        "بينما",
        "اذا",
        "إذا",
        "اواذا",
        "اوإذا",
        "والا",
        "وإلا",
        "احذف",
        "جلب",

        "دالة",
        "في",
        "ليس",
        "صنف",
        "من",
        //  الواجهة
        "صفحة",
        "نص",
        "رابط",
        "عمودي",
        "رأسي",
        "توسيط",
        "بطاقة",
    ];

    const نمط_الرموز = [
        { النوع: "مسافة", regex: /^[ \t]+/ },
        { النوع: "تعليق", regex: /^#.*\n?/ },
        { النوع: "سطر_جديد", regex: /^(\n|;|؛)/ },
        { النوع: "اقواس", regex: /^(\(|\)|\[|\]|\{|\})/ },
        { النوع: "فاصلة", regex: /^(,|،)/ },
        { النوع: "نقطة", regex: /^\./ },
        { النوع: "نقطتان", regex: /^:/ },
        { النوع: "رقم", regex: /^\-?\d+(\.\d+)?/ },
        { النوع: "نص", regex: /^م?(['"])(.*?)\1/ },
        {
            النوع: "رمز_حسابي",
            regex: /^(\+=|-=|\*=|\\=|\^=|\\\*=|\\\\=|==|!=|<=|>=|<|>|\+|\-|\*|\\\\|\\\*|\^|\\\^|\\|و|او|أو|ليس)/,
        },
        { النوع: "علامة_إسناد", regex: /^=/ },
        {
            النوع: "معرف",
            regex: /^[a-zA-Z\u0600-\u06FF_][a-zA-Z0-9\u0600-\u06FF_]*/,
        },
    ];

    let الرموز = [];
    let السطر = 1;
    let بداية_السطر = true;

    while (الشفرة.length > 0) {
        let متطابق = false;

        // معالجة المسافات فقط في بداية السطر
        const spaceMatch = الشفرة.match(نمط_الرموز[0].regex);
        if (spaceMatch) {
            const المسافة = spaceMatch[0];
            // إذا كنا في بداية السطر، نضيف مسافة للإندنتيشن
            if (بداية_السطر && المسافة.length > 1) {
                الرموز.push({ النوع: "مسافة", القيمة: المسافة, السطر });
            }
            // نتخطى المسافة دائماً
            الشفرة = الشفرة.slice(المسافة.length);
        }

        for (const النمط of نمط_الرموز) {
            if (النمط.النوع === "مسافة") continue;
            const تطابق = الشفرة.match(النمط.regex);
            if (تطابق) {
                const القيمة = تطابق[0];
                if (النمط.النوع === "تعليق") {
                    if (القيمة.includes("\n")) السطر++; // تجاهل التعليق
                    بداية_السطر = true;
                } else if (النمط.النوع === "سطر_جديد") {
                    الرموز.push({ النوع: "سطر_جديد", القيمة: "\n", السطر });
                    السطر++;
                    بداية_السطر = true;
                } else {
                    // كلمات محجوزة vs معرف عادي
                    if (النمط.النوع === "معرف" && الكلمات.includes(القيمة)) {
                        الرموز.push({ النوع: "كلمة", القيمة, السطر });
                    } else {
                        الرموز.push({ النوع: النمط.النوع, القيمة, السطر });
                    }
                    بداية_السطر = false;
                }

                الشفرة = الشفرة.slice(القيمة.length);
                متطابق = true;
                break;
            }
        }

        if (!متطابق) {
            throw new Error(
                `رمز غير معروف في السطر ${السطر}: ${الشفرة.slice(0, 10)}`
            );
        }
    }

    return الرموز;
}
